public class Sensor {

	private String sensorId;
	private String type;

	public String getName() {
		return sensorId;
	}

	public void setName(String name) {
		sensorId = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
