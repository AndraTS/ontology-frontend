package com.Beans;

public class OptimalConditions {
	private int minTemperature;
	private int maxTemperature;
	private int minMoisture;
	private int maxMoisture;
	private int minCo2;
	private int maxCo2;
	
	
	public int getMaxCo2() {
		return maxCo2;
	}
	public void setMaxCo2(int maxCo2) {
		this.maxCo2 = maxCo2;
	}
	public int getMinCo2() {
		return minCo2;
	}
	public void setMinCo2(int minCo2) {
		this.minCo2 = minCo2;
	}
	
	public int getMinTemperature() {
		return minTemperature;
	}
	public void setMinTemperature(int minTemperature) {
		this.minTemperature = minTemperature;
	}
	
	public int getMaxTemperature() {
		return maxTemperature;
	}
	public void setMaxTemperature(int maxTemperature) {
		this.maxTemperature = maxTemperature;
	}
	
	public int getMinMoisture() {
		return minMoisture;
	}
	public void setMinMoisture(int minMoisture) {
		this.minMoisture = minMoisture;
	}
	
	public int getMaxMoisture() {
		return maxMoisture;
	}
	public void setMaxMoisture(int maxMoisture) {
		this.maxMoisture = maxMoisture;
	}
	
	
}
